  id SERIAL PRIMARY KEY,
  choice_text VARCHAR(255) NOT NULL,
  answer BOOLEAN NOT NULL,
  question_id INT NOT NULL
);

--game_ageテーブルを作成
CREATE TABLE game_age (
  id SERIAL PRIMARY KEY,
  age_id INT NOT NULL,
  question_id INT NOT NULL
);

--inquiryテーブルを作成
