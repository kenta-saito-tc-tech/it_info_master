  id SERIAL PRIMARY KEY,
  inquiry_title VARCHAR(255) NOT NULL,
  inquiry_text TEXT NOT NULL,
  user_id INT NOT NULL,
  inquiry_answer TEXT,
  check_inquiry INT ,
  read_inquiry INT
