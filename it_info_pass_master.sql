--categoryテーブルを作成
CREATE TABLE category (
  id SERIAL PRIMARY KEY,
  category_name VARCHAR(255) NOT NULL
);

--category_selectテーブルを作成
CREATE TABLE category_select (
  id SERIAL PRIMARY KEY,
  user_id INT NOT NULL,
