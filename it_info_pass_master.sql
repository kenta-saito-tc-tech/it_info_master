--question_ageテーブルを作成
CREATE TABLE question_age (
  id SERIAL PRIMARY KEY,
  age_id INT NOT NULL,
  question_id INT NOT NULL
);

--questionsテーブルを作成
CREATE TABLE questions (
  id SERIAL PRIMARY KEY,
  question_name VARCHAR(255) NOT NULL,
  question_text TEXT NOT NULL,
  answer_text TEXT,
  category_id INT NOT NULL
